// The RegularCellData structure holds information about the triangulation
// used for a single equivalence class in the modified Marching Cubes algorithm,
// described in Section 3.2.

public struct RegularCellData
{
	// High nibble is vertex count, low nibble is triangle count.
	public byte GeometryCounts;
	
	// Groups of 3 indexes giving the triangulation.
	public int VertexIndex0;
	public int VertexIndex1;
	public int VertexIndex2;
	public int VertexIndex3;
	public int VertexIndex4;
	public int VertexIndex5;
	public int VertexIndex6;
	public int VertexIndex7;
	public int VertexIndex8;
	public int VertexIndex9;
	public int VertexIndex10;
	public int VertexIndex11;
	public int VertexIndex12;
	public int VertexIndex13;
	public int VertexIndex14;

	public int this[int index]
	{
		get
		{
			if (index == 0)
			{
				return VertexIndex0;
			}
			else if (index == 1)
			{
				return VertexIndex1;
			}
			else if (index == 2)
			{
				return VertexIndex2;
			}
			else if (index == 3)
			{
				return VertexIndex3;
			}
			else if (index == 4)
			{
				return VertexIndex4;
			}
			else if (index == 5)
			{
				return VertexIndex5;
			}
			else if (index == 6)
			{
				return VertexIndex6;
			}
			else if (index == 7)
			{
				return VertexIndex7;
			}
			else if (index == 8)
			{
				return VertexIndex8;
			}
			else if (index == 9)
			{
				return VertexIndex9;
			}
			else if (index == 10)
			{
				return VertexIndex10;
			}
			else if (index == 11)
			{
				return VertexIndex11;
			}
			else if (index == 12)
			{
				return VertexIndex12;
			}
			else if (index == 13)
			{
				return VertexIndex13;
			}
			else if (index == 14)
			{
				return VertexIndex14;
			}
			else
			{
				return -1;
			}
		}
	}
	
	public RegularCellData(byte geometryCounts, int vertexIndex0 = 0, int vertexIndex1 = 0, int vertexIndex2 = 0,
		int vertexIndex3 = 0, int vertexIndex4 = 0, int vertexIndex5 = 0, int vertexIndex6 = 0, int vertexIndex7 = 0,
		int vertexIndex8 = 0, int vertexIndex9 = 0, int vertexIndex10 = 0, int vertexIndex11 = 0, int vertexIndex12 = 0,
		int vertexIndex13 = 0, int vertexIndex14 = 0)
	{
		GeometryCounts = geometryCounts;
		VertexIndex0 = vertexIndex0;
		VertexIndex1 = vertexIndex1;
		VertexIndex2 = vertexIndex2;
		VertexIndex3 = vertexIndex3;
		VertexIndex4 = vertexIndex4;
		VertexIndex5 = vertexIndex5;
		VertexIndex6 = vertexIndex6;
		VertexIndex7 = vertexIndex7;
		VertexIndex8 = vertexIndex8;
		VertexIndex9 = vertexIndex9;
		VertexIndex10 = vertexIndex10;
		VertexIndex11 = vertexIndex11;
		VertexIndex12 = vertexIndex12;
		VertexIndex13 = vertexIndex13;
		VertexIndex14 = vertexIndex14;
		
	}
    
	public int GetVertexCount()
	{
		return (GeometryCounts >> 4);
	}
	
	public int GetTriangleCount()
	{
		return (GeometryCounts & 0x0F);
	}
};


